        <div class="col-10 p-3 rounded fs-5 my-4 bgColorText">
            <p>Dès 2005 est pensé une suite vidéoludique à Dofus, dans un système transmédia : Wakfu.
                Sa sortie initiale est prévue pour 2007. Dans la foulée, une nouvelle branche se crée, Ankama
                Animations, pour réaliser le dessin animé homonyme. Le 30 octobre 2008, la série télévisée
                d'animation
                Wakfu est diffusée pour la première fois sur France 3, dans l'émission de jeunesse Toowam. En
                novembre
                de l'année suivante, le premier jeu digital est créé : un jeu de cartes à collectionner nommé Wakfu
                TCG.
                Il regroupe les personnes de l'univers du Krosmoz (Dofus, Wakfu, etc.)</p>

            <p>Le premier court métrage d'Ankama Animations, Goultard le barbare, est diffusé à la Japan Expo en
                2007.
                Le 13 juin 2008, Ankama annonce un partenariat avec la chaîne de télévision Nolife, en proie à des
                difficultés financières, pour contribuer à son développement.</p>

            <p>Le 2 décembre 2009 la version de Dofus 2.0.À l'Ankama Convention 6, début mars 2011, les jeux vidéo
                Slage (dont le développement sera stoppé en 2012) et Boufbowl sont présentés. Le 30 mars 2011 sort
                Islands of Wakfu sur Xbox Live Arcade, premier jeu sur console. Le 13 mai de la même année, c'est le
                premier jeu sur appareils mobiles, Dofus: Battles, qui est créé.
                Le 29 février 2012, après quatre ans de retard, la suite de Dofus, le MMORPG Wakfu, sort.</p>
        </div>
