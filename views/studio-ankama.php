<div class="container-fluid">
    <div class="row">
        <div class="col-1"></div>
        <div class="col-10 p-3 rounded fs-5 my-4 bgColorText">
            <p>Ankama est créée le 15 mai 2001 et s'engage dans le développement web, à l'initiative de trois amis
                nordistes, Camille « Kam » Chafer (directeur technique), Anthony « Tot » Roux (directeur artistique)
                et Emmanuel Darras (directeur commercial), issus de la même agence web. Passionnés de jeux vidéo,
                ils ont associé leurs deux activités : le web et les jeux vidéo. Le nom de la société est issu de la
                contraction de leurs trois prénoms : ANthony, (C)KAmille, EmMAnuel.</p>

            <p>Dès 2003, l'entreprise compte une dizaine d'employés et via son pôle Ankama Studio, commence à
                développer son premier jeu, Dofus, un MMORPG. Il est réalisé en Flash et les bêta-test sortent dès
                le mois d'octobre. Le 1er septembre 2004, après presque un an de test, le jeu est distribué via
                Internet. Ankama connaît des difficultés pour le
                financement du jeu mais un apport salutaire de 15 000 euros du CNC aide sa création.
                En 2007, Dofus totalise plus de trois millions de joueurs à travers le monde.</p>


            <p>Fin 2005, face au refus de plusieurs éditeurs de publier certains de leurs ouvrages, Ankama lance en
                2005 sa propre maison d'édition, Ankama Éditions. Les premiers titres paraissent à un rythme d'une
                nouveauté tous les deux mois. Leurs premiers livres sont un artbook de
                Dofus sorti en juillet 2005 et un « manfra » (une bande dessinée d'inspiration asiatique) du nom de
                Dofus également. Elle est scénarisée par Anthony Roux et dessinée par Ancestral Z et Crounchan. Ses
                premiers tomes paraissent en octobre 2005 et en mars 2006. La série connaît un très grand succès
                (plus d'un million d'albums vendus en cinq ans) et est une précurseuse du genre.</p>
        </div>
    </div>
</div>

<div class="container-fluid">
